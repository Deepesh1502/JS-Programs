//Convert to String Explicit
let result;
//number to string
result = String(213);
  console.log(result);
result = String(1+3);
  console.log(result);
    
//other data types to string
result = String(null);
  console.log(result);
result = String(undefined);
  console.log(result);
result = String(NaN);
  console.log(result);
result = String(true);
  console.log(result);
result = String(false);
  console.log(result);
    
// using toString()
result = (213).toString();
  console.log(result);
result = true.toString();
  console.log(result);
